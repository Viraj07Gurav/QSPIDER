class do_while
{
    public static void main(String [] args)
    {
        int a=5;
        do
        {
            System.out.println(a);
            a--;
        }
        while(a>=-5);
        System.out.println("==================");
        int b=-11;
        do
        {
            System.out.println(b);
            b++;
        }
        while(b<=-1);
        System.out.println("==================");
        int c=1234;
        do
        {
            System.out.println(c);
            c--;
        }
        while(b>=1224);
        System.out.println("==================");
        int d=1111;
        do
        {
            System.out.println(d);
            d++;
        }
        while(d<=1121);
        System.out.println("==================");
        char ch='E';
        do
        {
            System.out.println(ch);
            ch++;
        }
        while(ch<='M');
        System.out.println("==================");

        char cr='p';
        do
        {
            System.out.println(cr);
            cr--;
        }
        while(cr>='m');
        System.out.println("==================");
    }
}