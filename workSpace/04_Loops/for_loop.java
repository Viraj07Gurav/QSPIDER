class for_loop
{
    public static void main(String[] args)
    {
        int a;
        for(a=-15;a<=-10;a++)
        {
            System.out.println(a);
        }
        System.out.println("======================");
        for(int b=-5;b>=-10;b--)
        {
            System.out.println(b);
        }
        System.out.println("======================");
        for(int c=-45;c<=41;c++)
        {
            System.out.println(c);
    
        
    }
        System.out.println("======================");
        for(int d=51;d<=59;d++)
        {
            System.out.println(d);
        }
        System.out.println("======================");
        for(char e='m';e<=-'p';e++)
        {
            System.out.println(e);
        }
        System.out.println("======================");
        for(char f='E';f>='A';f--)
        {
            System.out.println(f);
        }
        System.out.println("======================");
    }
}